//	 The Bitwise class.


class Bitwise {
  public static void main (String args[]) {
    int x = 5, y = 6;
    System.out.println("x = " + x);
    System.out.println("y = " + y);
    System.out.println("x & y = " + (x & y));
    System.out.println("x | y = " + (x | y));
    System.out.println("x ^ y = " + (x ^ y));
  }
}


/*
--------------------------------------------------------------------------------

Programýn çýktýsý þöyledir: 

x = 5
y = 6
x & y = 4
x | y = 7
x ^ y = 3

*/