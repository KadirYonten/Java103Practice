package Bölüm06;

public class Casting01 {

	public static void main(String[] args) {
        int x = 10;
        int y =3;

        System.out.println("x / y           = " + (x / y) );
        System.out.println("(int)(x / y)    = " + (int)(x / y));
        System.out.println("(float)(x / y)  = " + (float)(x / y) );
        System.out.println("(double)(x / y) = " + (double)(x / y));
        System.out.println("(float)x / y)   = " + ((float)x / y));
        System.out.println("(double)x / y)  = " + ((double)x / y));
	}
}
