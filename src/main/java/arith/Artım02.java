package Bölüm06;

public class Artım02 {

	public static void main(String[] args) {
        int x = 5;
        System.out.println("x = " + x);
        System.out.println("++x değeri = " + ++x);
        System.out.println("x = " + x);
        System.out.println("x++ değeri = " + x++);
        System.out.println("x = " + x);
	}
}
