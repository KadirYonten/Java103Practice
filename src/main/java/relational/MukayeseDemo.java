package Bölüm06;

public class MukayeseDemo {
		   public static void main(String[] args) {

		      //Sayılar
		      int i = 28;
		      int j = 35;
		      int k = 57;
		      System.out.println("sayıları yaz...");
		      System.out.println(" i = " + i);
		      System.out.println(" j = " + j);
		      System.out.println(" k = " + k);

		      //Daha büyük
		      System.out.println("Büyük mü?...");
		      System.out.println(" i > j = " + (i > j));
		      System.out.println(" j > i = " + (j > i));
		      System.out.println(" k > j = " + (k > j));
		      
		      //Büyük ya da eşit
		      System.out.println("Küçük ya da eşit mi?...");
		      System.out.println(" i >= j = " + (i >= j));
		      System.out.println(" j >= i = " + (j >= i));
		      System.out.println(" k >= j = " + (k >= j));
		      
		      //daha küçük
		      System.out.println("Küçük mü?...");
		      System.out.println(" i < j = " + (i < j));
		      System.out.println(" j < i = " + (j < i));
		      System.out.println(" k < j = " + (k < j));
		      
		      //daha küçük ya da eşit
		      System.out.println("daha küçük ya da eşit mi?...");
		      System.out.println(" i <= j = " + (i <= j));
		      System.out.println(" j <= i = " + (j <= i));
		      System.out.println(" k <= j = " + (k <= j));

		      //Eşit mi?
		      System.out.println("Eşit mi?...");
		      System.out.println(" i == j = " + (i == j));
		      System.out.println(" k == j = " + (k == j));
		      
		      //Eşit değil mi?
		      System.out.println("Eşit değil mi?...");
		      System.out.println(" i != j = " + (i != j));
		      System.out.println(" k != j = " + (k != j));
		   }
		}

